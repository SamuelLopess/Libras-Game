package br.memoria;

public class Carta {

	private int imagem;
	private int par;
	public boolean isFlip = false;
	public boolean isMatch = false;
	private int posicao;

	public Carta(int imagem, int par) {
		super();
		this.imagem = imagem;
		this.par = par;
	}
	
	public int getImagem() {
		return imagem;
	}
	
	public int getPar() {
		return par;
	}

	public void setPosicao(int posicao) {
		this.posicao = posicao;
	}
	
	public int getPosicao() {
		return posicao;
	}
	
}
